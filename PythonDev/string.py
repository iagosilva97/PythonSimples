movieName = "Top Gun"
# string [inicio:fim] - indice começa na posição 0 | indice final -1

#1 - buscar toda a string a partir da primeira posição
print(movieName[0:])

#2 - buscar toda a string até a ultima posição
print(movieName[:7])

#3 - buscar toda a string da terceira a ultima
print(movieName[2:])

#4 - buscar toda a string de dois em dois caracteres
print(movieName[::2])

#5 - buscar toda a string nos indices impares
print(movieName[1::2])

#6 - inverter toda a string de trás para frente
print(movieName[::-1])
